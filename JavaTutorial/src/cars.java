
public class cars {
	
	int speed;
	public cars(int startspeed){
		speed = startspeed;
	}
	
	
	
	public void increaseSpreed ()
	{
		speed++;
		System.out.println("Increasing speed");
	}

	public void decreaseSpreed ()
	{
		speed--;
		System.out.println("Decreasing speed");
	}

}
